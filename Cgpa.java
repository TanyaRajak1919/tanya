//WAP to calculate CGPA using marks of three subject (out of 100)
public class Cgpa {
    public static void main(String[] args) {
        float subject1 = 56;
        float subject2 = 67;
        float subject3 = 88;
        float cgpa = (subject1 + subject2 + subject3) / 30;
        System.out.println(cgpa);
    }

}
